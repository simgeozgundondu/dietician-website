export function getRecipeBySlug(slug, locale = 'tr') {
  const recipe = recipes.find(recipe => recipe.slug === slug)
  if (!recipe) return null
  
  const translations = recipe[locale] || recipe.tr
  return {
    ...recipe,
    title: translations.title,
    excerpt: translations.excerpt,
    content: translations.content,
    ingredients: translations.ingredients,
    instructions: translations.instructions
  }
}
